export function HeaderMenu({menu, viewport}) {
  const [root] = useMatches();
  const publicStoreDomain = root?.data?.publicStoreDomain;
  const className = `header-menu-${viewport}`;

  const TopmenuHover = event => {
    
    if(event.target.closest('.top-level-nav').querySelector('.mega-menu-full-image-dropdown')){
      let otherTopNav = document.querySelectorAll('.mega-menu-full-image-dropdown');
      otherTopNav.forEach(menu => {
        menu.classList.remove('active');
      })
      event.target.closest('.top-level-nav').querySelector('.mega-menu-full-image-dropdown').classList.add('active');
    } else{
      let otherTopNav = document.querySelectorAll('.mega-menu-full-image-dropdown');
      otherTopNav.forEach(menu => {
        menu.classList.remove('active');
      })
    }
    
  }

  const DropDownOut = event => {
    event.target.closest('.mega-menu-full-image-dropdown').classList.remove('active');
  }

  function closeAside(event) {
    if (viewport === 'mobile') {
      event.preventDefault();
      window.location.href = event.currentTarget.href;
    }
  }

  return (
    <nav className={className} role="navigation">
      
        <NavLink
          end
          onClick={closeAside}
          prefetch="intent"
          style={activeLinkStyle}
          to="/"
        >
          <svg className="w-5 h-5 text-gray-800 dark:text-white" aria-hidden="true" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 20 20">
            <path stroke="currentColor" strokeLinecap="round" strokeLinejoin="round" strokeWidth="2" d="M3 8v10a1 1 0 0 0 1 1h4v-5a1 1 0 0 1 1-1h2a1 1 0 0 1 1 1v5h4a1 1 0 0 0 1-1V8M1 10l9-9 9 9"/>
          </svg>
        </NavLink>
      
      {(menu || FALLBACK_HEADER_MENU).items.map((item) => {
        if (!item.url) return null;

        // if the url is internal, we strip the domain
        const url =
          item.url.includes('myshopify.com') ||
          item.url.includes(publicStoreDomain)
            ? new URL(item.url).pathname
            : item.url;
        return (
          <div className='top-menu' key={'top-menu-item--'+item.id} >
            <React.Fragment key={`fragment-${item.id}`}>
              <div className='top-level-nav'>
                <NavLink
                className="header-menu-item flex items-center justify-between"
                end
                key={item.id}
                onClick={closeAside}
                prefetch="intent"
                style={activeLinkStyle}
                to={url}
                onMouseEnter={TopmenuHover}
              >
                {item.title} 
                { item?.items.length > 0  && ( 
                  <svg className="w-6 h-6 px-2 menu-arrow" aria-hidden="true" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 10 6">
                    <path stroke="currentColor" strokeLinecap="round" strokeLinejoin="round" strokeWidth="2" d="m1 1 4 4 4-4"></path>
                  </svg>
                )}
              </NavLink>
              
                {/* { item?.items.length > 0  && ( 
                    <MegaMenu onMouseLeave={DropDownOut} subItem={item?.items} />
                )} */}
            </div>
            </React.Fragment>
      
          </div>
          
        );
      })}
    </nav>
  );
}

